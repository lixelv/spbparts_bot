from aiogram.types import ReplyKeyboardMarkup, KeyboardButton
from typing import Iterable


def box(iteable: Iterable, pack_size: int):
    it = iter(iteable)

    while True:
        try:
            box = []

            for _ in range(pack_size):
                box.append(next(it))

            yield box
        except StopIteration:
            if box:
                yield box

            break


